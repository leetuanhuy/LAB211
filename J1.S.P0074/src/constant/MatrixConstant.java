package constant;

/**
 * Contains all magic number constants used in the calculator program.
 */
public class MatrixConstant {
   
   public static final int ADD = 1;
    /** Menu option for subtraction */
    public static final int SUB = 2;
    /** Menu option for multiplication */
   public static final  int MUL = 3;
    /** Menu option for exiting the program */
    public static final int EXIT = 4;

    /** Minimum menu option value */
    public static final int MENU_MIN = 1;
    /** Maximum menu option value */
    public static final int MENU_MAX = 4;

    /** Minimum value for matrix row/column dimension */
    public static final int MIN_DIMENSION = 1;
    /** Maximum value for matrix row/column dimension */
    public static final int MAX_DIMENSION = 100;
}
